export function addTwoNumbers(num1: number, num2: number): number {
    return num1 + num2;
}

export function subTwoNumbers(num1: number, num2: number): number {
    return num1 - num2;
}

export function timeTwoNumbers(num1: number, num2: number): number {
    return num1 * num2;
}

export function divTwoNumbers(num1: number, num2: number): number {
    return num1 / num2;
}